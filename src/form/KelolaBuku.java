    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try {

        Connection conn = Koneksi.getConnection();

        String sql =
        "INSERT INTO buku(nama_buku, penulis, stok, harga) VALUES(?,?,?,?)";

        PreparedStatement ps =
        conn.prepareStatement(sql);

        ps.setString(1, txtNama.getText());
        ps.setString(2, txtPenulis.getText());
        ps.setInt(3, Integer.parseInt(txtStok.getText()));
        ps.setDouble(4, Double.parseDouble(txtHarga.getText()));

        ps.executeUpdate();

        JOptionPane.showMessageDialog(
                null,
                "Data Berhasil Ditambahkan"
        );

        tampilData();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                e.getMessage()
        );

    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         try {

        Connection conn = Koneksi.getConnection();

        String sql =
        "UPDATE buku SET nama_buku=?, penulis=?, stok=?, harga=? WHERE id_buku=?";

        PreparedStatement ps =
                conn.prepareStatement(sql);

        ps.setString(1, txtNama.getText());
        ps.setString(2, txtPenulis.getText());
        ps.setInt(3, Integer.parseInt(txtStok.getText()));
        ps.setDouble(4, Double.parseDouble(txtHarga.getText()));
        ps.setInt(5, Integer.parseInt(txtId.getText()));

        ps.executeUpdate();

        JOptionPane.showMessageDialog(
                null,
                "Data Berhasil Diubah"
        );

        tampilData();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                e.getMessage()
        );

    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        
    try {

        Connection conn = Koneksi.getConnection();

        String sql =
        "DELETE FROM buku WHERE id_buku=?";

        PreparedStatement ps =
                conn.prepareStatement(sql);

        ps.setInt(1,
                Integer.parseInt(txtId.getText()));

        ps.executeUpdate();

        JOptionPane.showMessageDialog(
                null,
                "Data Berhasil Dihapus"
        );

        tampilData();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                e.getMessage()
        );

    }
    }//GEN-LAST:event_btnHapusActionPerformed

