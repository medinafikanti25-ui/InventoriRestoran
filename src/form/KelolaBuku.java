    private void tampilData() {

    DefaultTableModel model =
            (DefaultTableModel) tblBuku.getModel();

    model.setRowCount(0);

    try {

        Connection conn =
                Koneksi.getConnection();

        Statement st =
                conn.createStatement();

        ResultSet rs =
                st.executeQuery("SELECT * FROM buku");

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_buku"),
                rs.getString("nama_buku"),
                rs.getString("penulis"),
                rs.getInt("stok"),
                rs.getDouble("harga")
            });

        }

    } catch (Exception e) {

        System.out.println(
                "Error : " + e.getMessage()
        );

    }
}

